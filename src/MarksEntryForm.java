    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        String usn = txtUSN.getText();
String subcode = txtSubCode.getText();

String cie1 = txtCIE1.getText();
String cie2 = txtCIE2.getText();
String cie3 = txtCIE3.getText();

String assignment = txtAssignment.getText();
String lab = txtLabMarks.getText();
String see = txtSEE.getText();

try
{
    Class.forName("com.mysql.cj.jdbc.Driver");

    java.sql.Connection con = java.sql.DriverManager.getConnection(
        "jdbc:mysql://localhost:3306/student_result_management",
        "root",
        "akanksha2006"
    );

    String query = "INSERT INTO marks (USN, Sub_code, CIE1, CIE2, CIE3, Assignment_Marks, Lab_Marks, SEE) VALUES (?,?,?,?,?,?,?,?)";

    java.sql.PreparedStatement pst = con.prepareStatement(query);

    pst.setString(1, usn);
    pst.setString(2, subcode);
    pst.setString(3, cie1);
    pst.setString(4, cie2);
    pst.setString(5, cie3);
    pst.setString(6, assignment);
    pst.setString(7, lab);
    pst.setString(8, see);

    pst.executeUpdate();
    System.out.println("Marks Inserted Successfully");
    txtUSN.setText("");
txtSubCode.setText("");
txtCIE1.setText("");
txtCIE2.setText("");
txtCIE3.setText("");
txtAssignment.setText("");
txtLabMarks.setText("");
txtSEE.setText("");
    PreparedStatement pst2;
ResultSet rs2;

pst2 = con.prepareStatement(
"SELECT COUNT(*) FROM marks WHERE usn=?"
);

pst2.setString(1, usn);

rs2 = pst2.executeQuery();

if(rs2.next())
{
    int count = rs2.getInt(1);

    if(count == 5)
    {
        java.sql.CallableStatement cst;

        cst = con.prepareCall(
        "{CALL Calculate_SGPA(?,?)}"
        );

        cst.setString(1, usn);

        cst.setInt(2, 4);

        cst.execute();

        JOptionPane.showMessageDialog(
        this,
        "Final Result Generated"
        );
    }
}

JOptionPane.showMessageDialog(
this,
"Marks Added Successfully"
);
}
catch(Exception e)
{
    JOptionPane.showMessageDialog(
        this,
        "Invalid USN or Subject Code!"
    );

    System.out.println(e);
}
    }//GEN-LAST:event_btnSubmitActionPerformed

