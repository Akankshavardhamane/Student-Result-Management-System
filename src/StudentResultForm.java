    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String usn = txtUSN.getText();

try
{
    Class.forName("com.mysql.cj.jdbc.Driver");

    java.sql.Connection con = java.sql.DriverManager.getConnection(
        "jdbc:mysql://localhost:3306/student_result_management",
        "root",
        "akanksha2006"
    );

    String query =
"SELECT Marks.Sub_code, Marks.CIE1, Marks.CIE2, Marks.CIE3, " +
"Marks.Assignment_Marks, Marks.Lab_Marks, Marks.SEE, " +
"Marks.Final_Total, Marks.Grade, Marks.Grade_Point, " +
"Subject.Credits " +
"FROM Marks " +
"JOIN Subject ON Marks.Sub_code = Subject.Sub_code " +
"WHERE Marks.USN = ?";

    java.sql.PreparedStatement pst = con.prepareStatement(
    query,
    java.sql.ResultSet.TYPE_SCROLL_INSENSITIVE,
    java.sql.ResultSet.CONCUR_READ_ONLY
);

    pst.setString(1, usn);

    java.sql.ResultSet rs = pst.executeQuery();

    javax.swing.table.DefaultTableModel model =
    new javax.swing.table.DefaultTableModel();

    model.addColumn("Subject");
    model.addColumn("CIE1");
    model.addColumn("CIE2");
    model.addColumn("CIE3");
    model.addColumn("Assignment");
    model.addColumn("Lab");
    model.addColumn("SEE");
    model.addColumn("Final");
    model.addColumn("Grade");
    model.addColumn("Grade Point");
    double totalPoints = 0;
int totalCredits = 0;


    while(rs.next())
    {
        int credits = rs.getInt("Credits");

totalPoints +=
rs.getDouble("Grade_Point") * credits;

totalCredits += credits;
        model.addRow(new Object[]
        {
            rs.getString("Sub_code"),
            rs.getInt("CIE1"),
            rs.getInt("CIE2"),
            rs.getInt("CIE3"),
            rs.getInt("Assignment_Marks"),
            rs.getInt("Lab_Marks"),
            rs.getInt("SEE"),
            rs.getInt("Final_Total"),
            rs.getString("Grade"),
            rs.getDouble("Grade_Point")
        });
    }

    tblResult.setModel(model);
   double sgpa =
totalPoints / totalCredits;

txtSGPA.setText(String.format("%.2f", sgpa));
}
catch(Exception e)
{
    System.out.println(e);
}
    }//GEN-LAST:event_btnSearchActionPerformed

