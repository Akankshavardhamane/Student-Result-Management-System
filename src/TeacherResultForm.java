    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        try
{
    Class.forName("com.mysql.cj.jdbc.Driver");

    java.sql.Connection con = java.sql.DriverManager.getConnection(
        "jdbc:mysql://localhost:3306/student_result_management",
        "root",
        "akanksha2006"
    );

    String query = "SELECT Student.USN, Student.Name, Result.SGPA, Result.CGPA FROM Student JOIN Result ON Student.USN = Result.USN";

    java.sql.PreparedStatement pst = con.prepareStatement(query);

    java.sql.ResultSet rs = pst.executeQuery();

    javax.swing.table.DefaultTableModel model =
    new javax.swing.table.DefaultTableModel();

    model.addColumn("USN");
    model.addColumn("Name");
    model.addColumn("SGPA");
    model.addColumn("CGPA");

    while(rs.next())
    {
        model.addRow(new Object[]
        {
            rs.getString("USN"),
            rs.getString("Name"),
            rs.getDouble("SGPA"),
            rs.getDouble("CGPA")
        });
    }

    tblAllResults.setModel(model);
}
catch(Exception e)
{
    System.out.println(e);
}
    }//GEN-LAST:event_formWindowOpened

